export const getMonth = (month: number) => {
    if(month === 1) return "كانون الثاني"
    else if(month === 2) return "شباط"
    else if(month === 3) return "آذار"
    else if(month === 4) return "نيسان"
    else if(month === 5) return "أيار"
    else if(month === 6) return "حزيران"
    else if(month === 7) return "تموز"
    else if(month === 8) return "آب"
    else if(month === 9) return "أيلول"
    else if(month === 10) return "تشرين الأول"
    else if(month === 11) return "تشرين الثاني"
    else if(month === 12) return "كانون الأول"
}